// programming language - it is a set of rules and syntax to write a program in a particular language
// compiler - it is a program that takes a program written in a programming language and converts it into a format that can be executed by the computer (in our case a binary file)
// interpreter - it is a program that takes a program written in a programming language and executes it line by line
// low-level-language - it is a programming language that is closer to machine language than human language
// c++ - it is a programming language, it is a compiled language, it follows the object-oriented programming paradigm, it is a general-purpose programming language
// high-level-language - it is a programming language that is easy to read and write, it is closer to human language than machine language



