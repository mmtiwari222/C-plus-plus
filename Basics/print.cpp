int main() // main function - it is the entry point of a c++ program
{ // scope of function
    cout << "Hello World\n"; // cout - it is an object of class iostream, it is used to print output to the console
    // << - it is an operator, it is used to send output to the console
    // ; - it is a statement terminator, it is used to mark the end of a statement
    // endl (escape sequence) (\n) - it is a manipulator, it is used to insert a new line and flush the output buffer
}